D=[1 2 4;1.5 2.3 5;2.1 2.5 5.6; 3 3.2 6.1; 4 2.6 6.2; 4.1 2.2 6;4.8 1.8 5.3;5 1 4];
A=ones(8,1);
A(:,2)=D(:,1);A(:,3)=D(:,2);A(:,4)=D(:,1).^2;A(:,5)=D(:,2).^2;A(:,6)=D(:,1).*D(:,2);
Y=D(:,3);
C=inv(A'*A)*A'*Y;
x=1:0.1:5.5;
y=1:0.1:5.5;
[x,y]=meshgrid(x,y);
graf=C(1)+C(2)*x+C(3)*y+C(4)*x.^2+C(5)*y.^2+C(6)*x.*y;
surf(x,y,graf)