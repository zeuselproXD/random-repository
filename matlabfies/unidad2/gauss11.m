function yss = gauss11
  a = 0;b = pi/2;n = 15;
  f = @(x) sin(x);su = 0;
  x = @(t) ((b-a)*t +b+a) /2;
  [u v] = legendre11(n);
  for i = 1:n
    su = su+ v(i)*f(x(u(i)));
  endfor
su=(b-a)/2*su
endfunction

